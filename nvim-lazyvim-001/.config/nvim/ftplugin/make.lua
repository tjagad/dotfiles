local opt = vim.opt

-- Use tabs instead of spaces
opt.expandtab = false

-- Indentation width for auto-indent
opt.shiftwidth = 8

-- Number of spaces per tab
opt.tabstop = 8

-- Enable C-style indentation
opt.cindent = true

-- Copy indent from the current line
opt.autoindent = true

-- Set maximum text width for wrapping
opt.textwidth = 80

-- Disable line wrapping
opt.wrap = false
